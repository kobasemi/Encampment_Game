	<h1>作品名</h1>
	Encampment Game By AI(EGBA)


	<h1>作品概要</h1>
昨今では若者のパソコン離れが進行している一方で、ITの重要性はますます高くなっている。</br>
アメリカの大統領であるオバマ氏も「これからの時代、プログラミングスキルは必須となる教養の一つとなっていく」と述べている。</br>
しかし一方でプログラミングを学ぶ際に、漠然とした目的しか持たないまま勉強を始めても中々定着するものではない。</br>
そこで「ゲームをしながらプログラミングスキルを高めることができる」をコンセプトに開発されたのがEGBAである。</br>
EGBAはプレイヤー自身が最もメジャーな言語の一つであるjavaを使ってAIを開発し、全国のプレイヤーと陣取りゲームで戦うことができる。</br>
ランダムに起きるイベントや、戦闘による敵プレイヤーの陣の奪取など不確定的要素も多く、AIの対応力が試される。</br>

	<h1>種類</h1>
	Webアプリケーション
	<h1>使用言語</h1>
	java,PHP,javascript
	<h1>サーバ</h1>
	CentOS6.6(Apache,Tomcat,Mysql)


	<h1>作品のアピールポイント</h1>

この作品のアピールポイントはオンライン対戦とマルチプラットフォームである。</br>
EGBA以外にも対人AIプログラミングゲームは存在するがそのほとんどがローカル環境内での使用に限られている。</br>
そのため、自分が作ったAIを他人のAIと戦わせるためには、そのための場を用意するか、または他人の作ったAIを別のところから入手するしかない。</br>
EGBAはAIを戦わせるためのサーバがインターネット上に存在するため、プレイヤーはすぐにでも自分が作ったAIを不特定多数のAIと戦わせることができる。</br>
またランキング機能も用意されており、自分のAIの相対的な強さの把握も容易である。
ユーザが使用する言語はOSに依存しないjavaであり、実行環境もサーバのためブラウザさえあれば遊ぶことができる。</br>


	<h1>プログラミングにおける工夫点</h1>
EGBAではプレイヤーがjavaの初学者であってもAIを作成することができるように、AI作成を補助するためのメソッドをいくつか用意している。</br>
AI作成の際はそのメソッドを用いることで、簡単なAIを作成することが可能となっている。またサーバ上でユーザが作成したプログラムが実行される点を考慮して、
悪意のあるコードを防ぐためのセキュリティ機能も実装している。EGBAはサーバでゲームの結果を算出し、ブラウザに送信している。サーバプログラムはログをJSONに変換してブラウザへと渡しているため、通信のデータ量は
極めて小さく、処理速度も早い。

